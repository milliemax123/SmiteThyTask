    // Updates the rows in the progresss table
    public void updateTable(List<Object[]> rows) {
        DefaultTableModel model = (DefaultTableModel) progressTable.getModel();
        model.setRowCount(0); // clear the old rows

        for (Object[] r : rows) {
            model.addRow(r);
        }
    }

